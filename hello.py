# Fundamental arithmetics
print(3 + 2)
print(3 - 2)
print(3 / 2)
print(3 * 2)
print('-------------------------------------')

# More arithmetics
print(3 // 2)
print(3 % 2)
print(3 ** 2)
print('-------------------------------------')

# Arithmetics on variables
x = 3
y = 2
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print('-------------------------------------')

# Assign arithmetic result to variable
z = x + y
print(z)
print('-------------------------------------')

# Printing the type of variables
x = 1
y = 1.0
print(type(x))
print(type(y))
print('-------------------------------------')

# Casting variable to different types
x = 1
x = float(x)
y = 1.0
y = int(y)
print(x)
print(y)


