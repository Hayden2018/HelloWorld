# Numeric comparison
x = 3
y = 4
print(x > 3)
print(x >= 3)
print(x < y)
print(x <= y)
print(y == 4)
print('-------------------------------------')

# Boolean variable
z = x > y
print(z)
print('-------------------------------------')

# String comparison
s = 'Hello'
print(s == 'Hello')
print(s == 'Hi')
print('-------------------------------------')

# If-else statement
n = 12590
if n % 3 == 0:
    print(n, 'is divisible by three')
elif n % 2 == 0:
    print(n, 'is divisible by two')
else:
    print(n, 'is not divisible by three')
